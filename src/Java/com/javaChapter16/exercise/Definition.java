public class Definition {
    /**
     * @author Whalewalker
     * What is Collection: The Collection in Java is a framework
     * that provides an architecture to store and manipulate the group of objects
     *
     * It is used to represent a group of individual objects as a single unit.
     *
     * ==============
     * What is Collections: Collections is a class in java that provide several
     * utility methods that are used to operate on collection.
     *
     *
     * ===============
     * What is Comparator: Java Comparator interface is
     * used to order the objects of a user-defined class.
     *
     * =========
     * What is List: List in Java provides the facility to maintain the ordered collection.
     * It contains the index-based methods to insert, update, delete and search the elements.
     * It can have the duplicate elements also.
     *
     * The List interface is found in the java.util package and inherits the Collection interface.
     * The implementation classes of List interface are ArrayList, LinkedList, Stack and Vector.
     * The ArrayList and LinkedList are widely used in Java programming.
     *
     * ==============
     * What is Load Factor:  The load factor is the measure that decides when to increase
     * the capacity of the Map.The default load factor is 75% of the capacity.
     *
     *================
     * What is collision: A collision occurs when a hash function returns the same bucket
     * location for two different keys.
     *
     * ================
     * What is a Space/ Time trade-off in hashing: a space-time or time-memory tradeoff
     * is a way ofsolving a problem or calculation in less time by using more storage
     * space (or memory), or by solving a problem in very little space by spending
     * a long time.
     *
     * ===============
     * What is Hashmap: Java HashMap is a hash table based implementation of Java's Map interface.
     * HashMap is an unordered collection. It does not guarantee any specific order of the elements.
     *
     * ===============
     * What is Vector: vector is like the dynamic array which can grow or shrink its size. Unlike array,
     * we can store n-number of elements in it as there is no size limit.
     *
     * -------VECTOR METHODS -------------
     * ADD: It is used to append the specified element in the given vector.
     *
     * SET:It is used to replace the element at the specified position in
     * the vector with the specified element.
     *
     * REMOVE: It is used to remove the specified element from the vector.
     * If the vector does not contain the element, it is unchanged.
     *
     * REMOVE_ALL_ELEMENTS: It is used to remove all elements from the vector
     * and set the size of the vector to zero.
     *
     * REMOVE_ELEMENT_AT: It is used to delete the component at the specified index.
     *
     * FIRST_ELEMENT: It is used to get the first component of the vector.
     *
     * LAST_ELEMENT: It is used to get the last component of the vector.
     *
     * CONTAINS: It returns true if the vector contains the specified element.
     *
     * INDEX_OF: It is used to get the index of the first occurrence of the specified
     * element in the vector. It returns -1 if the vector does not contain the element.
     *
     * SIZE: It is used to get the number of components in the given vector.
     *
     * CAPACITY: It is used to get the current capacity of this vector.
     */
}
